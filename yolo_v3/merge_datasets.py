"""
YOLO Dataset Merger — Combine per-object datasets into multi-class training sets.

Merges multiple single-class YOLO datasets (generated by generate_yolo_v3.py)
into a unified multi-class dataset with sequential class IDs and a single
data.yaml.

Usage:
    # Merge specific object folders
    python merge_datasets.py --sources C:/data/cam_front/gate_searchrescue C:/data/cam_front/octagon --output C:/merged/

    # Merge all sub-folders under a camera group
    python merge_datasets.py --source_root C:/data/ --groups cam_front --output C:/merged_front/

    # Merge everything
    python merge_datasets.py --source_root C:/data/ --all --output C:/merged_all/

    # Dry-run (preview without writing)
    python merge_datasets.py --source_root C:/data/ --all --output C:/merged/ --dry_run

Output:
    output/
    ├── data.yaml           (unified, sequential class IDs)
    ├── train/
    │   ├── images/         (prefixed: {source}_{original}.png)
    │   └── labels/         (remapped class IDs)
    └── val/
        ├── images/
        └── labels/

No UE5 dependency — runs with standard Python.
"""

import argparse
import os
import sys
import shutil
import glob

# Add script directory to path so we can import object_registry
_script_dir = os.path.dirname(os.path.abspath(__file__))
if _script_dir not in sys.path:
    sys.path.insert(0, _script_dir)

from object_registry import OBJECT_DEFS, CAMERA_GROUPS


def parse_data_yaml(yaml_path):
    """Parse a data.yaml file and return class names dict {id: name} and task."""
    class_names = {}
    task = "detect"
    if not os.path.exists(yaml_path):
        return class_names, task

    parsing_names = False
    with open(yaml_path, 'r') as f:
        for line in f:
            line = line.strip()
            if line.startswith("task:"):
                task = line.split(":", 1)[1].strip()
            if line.startswith("names:"):
                parsing_names = True
                continue
            if parsing_names:
                if not line or line.startswith("#"):
                    continue
                if ":" in line:
                    parts = line.split(":", 1)
                    try:
                        idx = int(parts[0].strip())
                        name = parts[1].strip().strip("'\"")
                        class_names[idx] = name
                    except ValueError:
                        parsing_names = False
                else:
                    parsing_names = False
    return class_names, task


def discover_sources(source_root, groups=None, select_all=False):
    """Discover per-object dataset folders under source_root.

    Returns list of (source_path, object_name) tuples.
    """
    sources = []

    if select_all:
        # Scan all camera group folders
        target_groups = list(CAMERA_GROUPS.keys())
    elif groups:
        target_groups = groups
    else:
        return sources

    for group_name in target_groups:
        group_dir = os.path.join(source_root, group_name)
        if not os.path.isdir(group_dir):
            print(f"  WARNING: Group folder not found: {group_dir}")
            continue
        for item in sorted(os.listdir(group_dir)):
            item_path = os.path.join(group_dir, item)
            yaml_path = os.path.join(item_path, "data.yaml")
            if os.path.isdir(item_path) and os.path.exists(yaml_path):
                sources.append((item_path, item))

    return sources


def remap_label_file(src_label_path, dst_label_path, old_to_new_id_map):
    """Read a YOLO label file, remap class IDs, write to destination."""
    lines = []
    with open(src_label_path, 'r') as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            parts = line.split()
            try:
                old_id = int(parts[0])
                new_id = old_to_new_id_map.get(old_id, old_id)
                parts[0] = str(new_id)
                lines.append(" ".join(parts))
            except (ValueError, IndexError):
                lines.append(line)  # pass through unparseable lines

    with open(dst_label_path, 'w') as f:
        f.write("\n".join(lines) + ("\n" if lines else ""))


def merge_datasets(sources, output_dir, dry_run=False):
    """Merge multiple per-object YOLO datasets into one multi-class dataset.

    Args:
        sources: List of (source_path, object_name) tuples
        output_dir: Path to write the merged dataset
        dry_run: If True, only print what would happen
    """
    print("=" * 60)
    print("YOLO DATASET MERGER")
    print("=" * 60)

    if not sources:
        print("ERROR: No source datasets found!")
        return

    # --- Step 1: Collect class names from all sources ---
    all_class_names = set()
    source_info = []
    task_type = "detect"

    for src_path, obj_name in sources:
        yaml_path = os.path.join(src_path, "data.yaml")
        class_names, task = parse_data_yaml(yaml_path)
        if task == "segment":
            task_type = "segment"

        if not class_names:
            # Fallback: use the folder name as class name
            class_names = {0: obj_name}

        for name in class_names.values():
            all_class_names.add(name)

        source_info.append({
            "path": src_path,
            "obj_name": obj_name,
            "local_classes": class_names,
        })

    # --- Step 2: Build global class mapping (registry class_id, fallback alphabetical) ---
    global_id_map = {}
    for name in all_class_names:
        if name in OBJECT_DEFS and "class_id" in OBJECT_DEFS[name]:
            global_id_map[name] = OBJECT_DEFS[name]["class_id"]

    # Any names not in the registry get sequential IDs after the highest registered ID
    next_id = max(global_id_map.values(), default=-1) + 1
    for name in sorted(all_class_names - set(global_id_map)):
        global_id_map[name] = next_id
        next_id += 1

    global_classes = sorted(global_id_map.keys(), key=lambda n: global_id_map[n])

    print(f"\nSources: {len(sources)}")
    print(f"Task: {task_type}")
    print(f"Global classes ({len(global_classes)}):")
    for name in global_classes:
        print(f"  {global_id_map[name]}: {name}")

    # --- Step 3: Build per-source remapping ---
    for info in source_info:
        old_to_new = {}
        for old_id, name in info["local_classes"].items():
            old_to_new[old_id] = global_id_map[name]
        info["remap"] = old_to_new
        print(f"\n  {info['obj_name']}: {info['path']}")
        print(f"    Local → Global: {old_to_new}")

    if dry_run:
        print("\n[DRY RUN] Would merge the above sources. No files written.")
        # Count what would be merged
        for split in ("train", "val"):
            total = 0
            for info in source_info:
                img_dir = os.path.join(info["path"], split, "images")
                if os.path.isdir(img_dir):
                    total += len(glob.glob(os.path.join(img_dir, "*.png")))
            print(f"  {split}: ~{total} images")
        return

    # --- Step 4: Create output directories ---
    if os.path.exists(output_dir):
        shutil.rmtree(output_dir)

    for split in ("train", "val"):
        os.makedirs(os.path.join(output_dir, split, "images"), exist_ok=True)
        os.makedirs(os.path.join(output_dir, split, "labels"), exist_ok=True)

    # --- Step 5: Copy and remap ---
    total_images = {"train": 0, "val": 0}
    total_labels = {"train": 0, "val": 0}
    collisions = 0

    for info in source_info:
        prefix = info["obj_name"]
        remap = info["remap"]

        for split in ("train", "val"):
            src_img_dir = os.path.join(info["path"], split, "images")
            src_lbl_dir = os.path.join(info["path"], split, "labels")
            dst_img_dir = os.path.join(output_dir, split, "images")
            dst_lbl_dir = os.path.join(output_dir, split, "labels")

            if not os.path.isdir(src_img_dir):
                continue

            for img_path in glob.glob(os.path.join(src_img_dir, "*.png")):
                basename = os.path.splitext(os.path.basename(img_path))[0]
                new_basename = f"{prefix}_{basename}"

                # Check for collision (shouldn't happen with prefix, but be safe)
                dst_img = os.path.join(dst_img_dir, f"{new_basename}.png")
                if os.path.exists(dst_img):
                    collisions += 1
                    new_basename = f"{prefix}_{collisions:04d}_{basename}"
                    dst_img = os.path.join(dst_img_dir, f"{new_basename}.png")

                shutil.copy2(img_path, dst_img)
                total_images[split] += 1

                # Remap and copy label
                src_lbl = os.path.join(src_lbl_dir, f"{basename}.txt")
                if os.path.exists(src_lbl):
                    dst_lbl = os.path.join(dst_lbl_dir, f"{new_basename}.txt")
                    remap_label_file(src_lbl, dst_lbl, remap)
                    total_labels[split] += 1

        print(f"  Merged: {prefix}")

    # --- Step 6: Generate unified data.yaml ---
    yaml_path = os.path.join(output_dir, "data.yaml")
    with open(yaml_path, "w") as f:
        f.write(f"path: {output_dir.rstrip('/').rstrip(chr(92))}\n")
        f.write("train: train/images\n")
        f.write("val: val/images\n")
        f.write(f"task: {task_type}\n")
        f.write(f"nc: {len(global_classes)}\n")
        f.write("names:\n")
        for name in global_classes:
            f.write(f"  {global_id_map[name]}: {name}\n")

    # --- Summary ---
    print("\n" + "=" * 60)
    print("MERGE COMPLETE")
    print(f"  Output: {output_dir}")
    print(f"  Classes: {len(global_classes)}")
    print(f"  Train: {total_images['train']} images, {total_labels['train']} labels")
    print(f"  Val:   {total_images['val']} images, {total_labels['val']} labels")
    if collisions:
        print(f"  Filename collisions resolved: {collisions}")
    print(f"  data.yaml: {yaml_path}")
    print("=" * 60)


def main():
    parser = argparse.ArgumentParser(
        description="Merge per-object YOLO datasets into multi-class training sets.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  python merge_datasets.py --sources path/to/gate_searchrescue path/to/octagon --output C:/merged/
  python merge_datasets.py --source_root C:/UE5_YOLO_Data_V3/ --groups cam_front --output C:/front/
  python merge_datasets.py --source_root C:/UE5_YOLO_Data_V3/ --all --output C:/all/
  python merge_datasets.py --source_root C:/UE5_YOLO_Data_V3/ --all --output C:/all/ --dry_run
        """)

    # Source selection (mutually exclusive approaches)
    parser.add_argument("--sources", nargs="+", type=str, default=None,
                        help="Explicit paths to per-object dataset folders")
    parser.add_argument("--source_root", type=str, default=None,
                        help="Root folder containing camera_group/object/ structure")
    parser.add_argument("--groups", nargs="+", type=str, default=None,
                        help="Camera groups to merge (requires --source_root)")
    parser.add_argument("--all", action="store_true",
                        help="Merge all groups under --source_root")

    parser.add_argument("--output", type=str, required=True,
                        help="Output directory for merged dataset")
    parser.add_argument("--dry_run", action="store_true",
                        help="Preview merge without writing files")

    args = parser.parse_args()

    # Resolve sources
    sources = []

    if args.sources:
        # Explicit paths mode
        for src in args.sources:
            if not os.path.isdir(src):
                print(f"WARNING: Source folder not found: {src}")
                continue
            obj_name = os.path.basename(src.rstrip("/\\"))
            sources.append((src, obj_name))
    elif args.source_root:
        # Auto-discover mode
        sources = discover_sources(args.source_root, args.groups, args.all)
    else:
        parser.error("Provide either --sources or --source_root with --groups/--all")

    if not sources:
        print("ERROR: No valid source datasets found!")
        sys.exit(1)

    merge_datasets(sources, args.output, dry_run=args.dry_run)


if __name__ == "__main__":
    main()
